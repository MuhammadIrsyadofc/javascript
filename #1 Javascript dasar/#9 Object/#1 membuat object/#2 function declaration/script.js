function buatObjectMahasiswa (nama,nrp,email,jurusan){
    var mhs = {} ;
    mhs.nama = nama ;
    mhs.nrp = nrp ;
    mhs.email = email ;
    mhs.jurusan = jurusan ;
    return mhs ;
}
